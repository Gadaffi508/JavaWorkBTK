public class fourProcess
{
    public int Collect(int number1,int number2)
    {
        return number1 + number2;//Toplama
    }
    public int Extraction(int number1,int number2)
    {
        return number1 - number2;//Çıkarma
    }
    public int Divide(int number1,int number2)
    {
        return number1 / number2;//Bölme
    }
    public int İmpact(int number1,int number2)
    {
        return number1 * number2;//çarpma
    }


}
